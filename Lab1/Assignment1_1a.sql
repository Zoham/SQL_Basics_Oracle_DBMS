-- Table COURSE --------------------
CREATE TABLE COURSE(
	Course_name VARCHAR(30) NOT NULL,
	Course_number VARCHAR(15) NOT NULL,
	Credit_hours INTEGER NOT NULL,
	Department VARCHAR(10) NOT NULL,
	PRIMARY KEY (Course_number));

