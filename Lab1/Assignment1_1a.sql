-- Table STUDENT --------------------
CREATE TABLE STUDENT (
	Name VARCHAR(30) NOT NULL,
	Student_number INTEGER NOT NULL,
	Class INTEGER NOT NULL,
	Major VARCHAR(10) NOT NULL,
	PRIMARY KEY (Student_number));

